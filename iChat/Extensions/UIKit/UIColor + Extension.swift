import UIKit

extension UIColor {
    
    static func buttonRed() -> UIColor {
        return UIColor(hex: "D0021B")
    }
    
    static func mainWhite() -> UIColor {
        return UIColor(hex: "F7F8FD")
    }
    
    static func buttonDark() -> UIColor {
        return UIColor(hex: "333333")
    }
    
}
